class MenuProperties:
    width = 800
    height = 600

    colorR = 48
    colorG = 48
    colorB = 54
    screenColor = (colorR, colorG, colorB)

    def getCenterX(self):
        return self.width / 2

    def getCenterY(self):
        return self.height / 2


    def setColorR(self, newColor):
        self.colorR = newColor

    def setColorG(self, newColor):
        self.colorR = newColor

    def setColorB(self, newColor):
        self.colorR = newColor
